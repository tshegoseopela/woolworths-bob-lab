#!/bin/bash
################################################################################
# Automated Code Review Script
# 
# This script performs automated code review on changed files in the current
# branch compared to a target branch (default: main).
#
# Usage:
#   ./code-review.sh [target-branch]
#
# Example:
#   ./code-review.sh main
#   ./code-review.sh develop
#
# Features:
# - Reviews all changed code files
# - Generates individual review reports
# - Creates summary report
# - Timestamps all reports
# - Supports multiple programming languages
################################################################################

set -e  # Exit on error

# Configuration
BRANCH="${1:-main}"
OUTPUT_DIR="./review-reports"
TIMESTAMP=$(date +%Y%m%d_%H%M%S)
SUPPORTED_EXTENSIONS="\.(js|jsx|ts|tsx|py|java|go|rb|php|cs|cpp|c|h)$"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Functions
print_header() {
    echo -e "${BLUE}================================${NC}"
    echo -e "${BLUE}$1${NC}"
    echo -e "${BLUE}================================${NC}"
}

print_success() {
    echo -e "${GREEN}✓ $1${NC}"
}

print_warning() {
    echo -e "${YELLOW}⚠ $1${NC}"
}

print_error() {
    echo -e "${RED}✗ $1${NC}"
}

# Check if Bob CLI is installed
if ! command -v bob &> /dev/null; then
    print_error "Bob CLI is not installed"
    echo "Install it with: npm install -g @ibm/bob-cli"
    exit 1
fi

# Check if we're in a git repository
if ! git rev-parse --git-dir > /dev/null 2>&1; then
    print_error "Not a git repository"
    exit 1
fi

# Create output directory
mkdir -p "$OUTPUT_DIR"

print_header "Automated Code Review"
echo "Target branch: $BRANCH"
echo "Output directory: $OUTPUT_DIR"
echo "Timestamp: $TIMESTAMP"
echo ""

# Check if target branch exists
if ! git rev-parse --verify "$BRANCH" > /dev/null 2>&1; then
    print_error "Branch '$BRANCH' does not exist"
    exit 1
fi

# Get list of changed files
print_header "Finding Changed Files"
CHANGED_FILES=$(git diff --name-only "$BRANCH"...HEAD | grep -E "$SUPPORTED_EXTENSIONS" || true)

if [ -z "$CHANGED_FILES" ]; then
    print_warning "No code files changed"
    echo "Compared against: $BRANCH"
    exit 0
fi

# Count files
FILE_COUNT=$(echo "$CHANGED_FILES" | wc -l)
print_success "Found $FILE_COUNT file(s) to review"
echo ""

# Display files to review
echo "Files to review:"
echo "$CHANGED_FILES" | while read -r file; do
    echo "  - $file"
done
echo ""

# Review each file
print_header "Reviewing Files"
REVIEW_COUNT=0
FAILED_COUNT=0

echo "$CHANGED_FILES" | while read -r file; do
    if [ -f "$file" ]; then
        echo -n "Reviewing: $file ... "
        
        # Generate safe filename for report
        SAFE_FILENAME=$(echo "$file" | tr '/' '_' | tr '.' '_')
        REPORT_FILE="$OUTPUT_DIR/${SAFE_FILENAME}_review_$TIMESTAMP.md"
        
        # Run Bob review
        if bob "Review $file for code quality, best practices, and potential issues. Provide detailed feedback in markdown format." > "$REPORT_FILE" 2>/dev/null; then
            print_success "Done"
            REVIEW_COUNT=$((REVIEW_COUNT + 1))
        else
            print_error "Failed"
            FAILED_COUNT=$((FAILED_COUNT + 1))
        fi
    else
        print_warning "File not found: $file (may have been deleted)"
    fi
done

echo ""

# Generate summary report
print_header "Generating Summary Report"
SUMMARY_FILE="$OUTPUT_DIR/summary_$TIMESTAMP.md"

cat > "$SUMMARY_FILE" << EOF
# Code Review Summary

**Date:** $(date)
**Branch:** $BRANCH
**Reviewed Files:** $FILE_COUNT
**Successful Reviews:** $REVIEW_COUNT
**Failed Reviews:** $FAILED_COUNT

## Files Reviewed

EOF

# Add list of reviewed files
echo "$CHANGED_FILES" | while read -r file; do
    if [ -f "$file" ]; then
        SAFE_FILENAME=$(echo "$file" | tr '/' '_' | tr '.' '_')
        REPORT_FILE="${SAFE_FILENAME}_review_$TIMESTAMP.md"
        echo "- [$file](./$REPORT_FILE)" >> "$SUMMARY_FILE"
    fi
done

cat >> "$SUMMARY_FILE" << EOF

## Review Reports

Individual review reports are available in the \`$OUTPUT_DIR\` directory.

## Next Steps

1. Review individual file reports for detailed feedback
2. Address high-priority issues first
3. Run tests after making changes
4. Re-run this script to verify improvements

---

*Generated by Bob Automated Code Review*
EOF

print_success "Summary report created: $SUMMARY_FILE"
echo ""

# Generate consolidated issues report
print_header "Analyzing Issues"
ISSUES_FILE="$OUTPUT_DIR/issues_$TIMESTAMP.md"

cat > "$ISSUES_FILE" << EOF
# Consolidated Issues Report

**Date:** $(date)
**Branch:** $BRANCH

## Critical Issues

EOF

# Extract critical issues from all reports (simplified - in real implementation would parse markdown)
find "$OUTPUT_DIR" -name "*_review_$TIMESTAMP.md" -type f | while read -r report; do
    if grep -q "Critical" "$report" 2>/dev/null; then
        FILE=$(basename "$report" | sed "s/_review_$TIMESTAMP.md//")
        echo "### $FILE" >> "$ISSUES_FILE"
        grep -A 5 "Critical" "$report" >> "$ISSUES_FILE" 2>/dev/null || true
        echo "" >> "$ISSUES_FILE"
    fi
done

cat >> "$ISSUES_FILE" << EOF

## High Priority Issues

EOF

find "$OUTPUT_DIR" -name "*_review_$TIMESTAMP.md" -type f | while read -r report; do
    if grep -q "High" "$report" 2>/dev/null; then
        FILE=$(basename "$report" | sed "s/_review_$TIMESTAMP.md//")
        echo "### $FILE" >> "$ISSUES_FILE"
        grep -A 5 "High" "$report" >> "$ISSUES_FILE" 2>/dev/null || true
        echo "" >> "$ISSUES_FILE"
    fi
done

print_success "Issues report created: $ISSUES_FILE"
echo ""

# Final summary
print_header "Review Complete"
echo "Reports saved to: $OUTPUT_DIR"
echo ""
echo "Summary: $SUMMARY_FILE"
echo "Issues: $ISSUES_FILE"
echo ""

if [ "$FAILED_COUNT" -gt 0 ]; then
    print_warning "$FAILED_COUNT file(s) failed review"
    exit 1
else
    print_success "All files reviewed successfully!"
    exit 0
fi

